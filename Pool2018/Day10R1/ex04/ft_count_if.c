int	ft_count_if(char **tab, int (*f)(char *))
{
	int	i;
	int	ctr;

	i = 0;
	ctr = 0;
	while (tab[i] != 0)
	{
		if (f(tab[i]) == 1)
			ctr++;
		++i;
	}
	return (ctr);
}
